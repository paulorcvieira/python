"""
+ Adição
- Subtração
/ Divisão
// Resultado da divisão é aredondado
** Potenciação
% Módulo/Resto da divisão
() Altera a precedência
"""

print('Multiplicação 10 * 10: ', 10 * 10)
print('Multiplicação 10 * 10: ', 10 * '10')
print('Adição 10 + 10', 10 + 10)
print('Adição "10" + "10"', '10' + '10')
print('Subtração 10 - 10', 10 - 10)
print('Divisão 10 / 10', 10 / 10)
print('Divisão 10 / 10', 10 / 3)
print('Divisão aredondada 10 // 10', 16 // 10)
print('Divisão aredondada 10 // 10', 15.6 // 10)
print('Protenciação 2 ** 10', 2 ** 10)
print('Protenciação 2 ** 10', 2 ** 10)
print('Módulo/Resto 10 % 5', 10 % 5)
print('Módulo/Resto 10 % 5', 13 % 5)
print('Precedência 5 + 2 * 10', 5 + 2 * 10)
print('Precedência (5 + 2) * 10)', (5 + 2) * 10)
